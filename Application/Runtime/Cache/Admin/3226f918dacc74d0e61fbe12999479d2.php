        $.post("<?php echo U('Base/comDel');?>",{"id":id,"tableName":"nav"},function (res) {
            if (res.status=="y") {
                $(obj).parents("tr").remove();
                layer.msg('已删除!',{icon:1,time:1000});
            };
        });

    });
}

