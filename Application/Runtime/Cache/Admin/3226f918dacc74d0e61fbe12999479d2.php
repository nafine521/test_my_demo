function saveVal(obj,id,field_name){
    var field_new=$(obj).val();
    $.post("<?php echo U('Base/status');?>",{"tableName":'nav',"fieldName":field_name,"primary":'id','id':id,"fieldVal":field_new},function(res){
        if (res.status=="y") {
            layer.msg('已修改!',{icon:1,time:1000});
        }
    })
}
